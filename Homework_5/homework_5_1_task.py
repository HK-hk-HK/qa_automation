"""
Напишіть функцію, що приймає один аргумант.
Функція має вивести на екран тип цього аргумента (для визначення типу використайте type)
"""

def print_type(argument):
    print(type(argument))
    return print(argument)


print_type(argument=876)
print_type(argument='test')
print_type(argument=5.7)
print_type(argument=[])



